function isActive(pathname: string, href: string): boolean {
  if (href === "/") return pathname === "/";
  return pathname === href || pathname.startsWith(`${href}/`);
}

export function Sidebar() {
  const pathname = usePathname();
  const [store, setStore] = useState<StoreInfo>({
    name: "Restaurant",
    logo_data_url: null,
  });
  const [username, setUsername] = useState<string | null>(null);

  useEffect(() => {
    fetch("/api/restaurant")
      .then((r) => r.json())
      .then((data: StoreInfo) => setStore(data))
      .catch(() => {});

    fetch("/api/auth/me")
      .then((r) => (r.ok ? r.json() : null))
      .then((data: { username: string } | null) => {
        if (data?.username) setUsername(data.username);
      })
      .catch(() => {});
  }, []);

  async function handleLogout() {
    await fetch("/api/auth/login", { method: "DELETE" });
    window.location.href = "/login";
  }

  return (
    <aside className="flex h-screen w-56 shrink-0 flex-col bg-[#1c1c1e] text-white">
      <Link
        href="/"
        className="flex shrink-0 items-center gap-3 border-b border-white/10 px-4 py-4 transition-colors hover:bg-white/5"
      >
        <StoreLogo
          src={store.logo_data_url}
          alt={`${store.name} logo`}
          size="md"
        />
        <div className="min-w-0">
          <p className="truncate text-sm font-semibold leading-tight">
            {store.name}
          </p>
          <p className="text-xs tracking-wide text-white/50">TableTalk AI</p>
        </div>
      </Link>

      <nav className="min-h-0 flex-1 space-y-0.5 overflow-y-auto px-3 py-4">
        {navItems.map((item) => {
          const active = isActive(pathname, item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
                active
                  ? "bg-white/10 text-white"
                  : "text-white/60 hover:bg-white/5 hover:text-white"
              }`}
            >
              <span className={active ? "text-teal-400" : ""}>{item.icon}</span>
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="shrink-0 border-t border-white/10 px-4 py-4">
        <div className="flex items-start gap-3">
          <StoreLogo
            src={store.logo_data_url}
            alt=""
            size="sm"
            className="mt-0.5"
          />
          <div className="min-w-0 flex-1">
            <p className="text-xs font-medium uppercase tracking-wide text-white/40">
              Account
            </p>
            <p className="mt-1 truncate text-sm text-white/70">
              {username ?? "Restaurant Owner"}
            </p>
            <button
              type="button"
              onClick={handleLogout}
              className="mt-2 text-xs text-white/40 transition-colors hover:text-white/70"
            >
              Sign out
            </button>
          </div>
        </div>
      </div>
    </aside>
  );
}
